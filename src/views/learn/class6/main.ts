import { initShaderProgram } from "@/util/webgl";
import fsSource from "./fs.frag?raw";
import vsSource from "./vs.vert?raw";
import { M4 } from "@/util/matrix";

export function main(gl: WebGL2RenderingContext) {
  const program = initShaderProgram(gl, vsSource, fsSource);
  if (!program) {
    return;
  }
  gl.useProgram(program);

  const positionAttributeLocation = gl.getAttribLocation(program, "a_position");
  const colorAttributeLocation = gl.getAttribLocation(program, "a_color");

  const vao = gl.createVertexArray();
  gl.bindVertexArray(vao);
  const positionBuffer = gl.createBuffer();
  gl.bindBuffer(gl.ARRAY_BUFFER, positionBuffer);
  gl.enableVertexAttribArray(positionAttributeLocation);
  gl.vertexAttribPointer(positionAttributeLocation, 3, gl.FLOAT, false, 0, 0);
  setGeometry(gl);

  const colorBuffer = gl.createBuffer();
  gl.bindBuffer(gl.ARRAY_BUFFER, colorBuffer);
  gl.enableVertexAttribArray(colorAttributeLocation);
  gl.vertexAttribPointer(
    colorAttributeLocation,
    3,
    gl.UNSIGNED_BYTE,
    true,
    0,
    0
  );
  setColors(gl);

  gl.viewport(0, 0, gl.canvas.width, gl.canvas.height);

  render(gl, program, 0);
}

function render(
  gl: WebGL2RenderingContext,
  program: WebGLProgram,
  fieldOfViewRadians: number
) {
  gl.clearColor(0, 0, 0, 0);
  gl.clear(gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);
  gl.enable(gl.DEPTH_TEST);
  gl.enable(gl.CULL_FACE);

  const aspect = gl.canvas.width / gl.canvas.height;
  const zNear = 1;
  const zFar = 2000;
  let matrix = M4.perspective(fieldOfViewRadians, aspect, zNear, zFar);
  matrix = M4.translate(matrix, 0, 0, -360);
  matrix = M4.xRotate(matrix, 190);
  matrix = M4.yRotate(matrix, 40);
  matrix = M4.zRotate(matrix, 320);
  gl.useProgram(program);
  const matrixLocation = gl.getUniformLocation(program, "u_matrix")!;
  gl.uniformMatrix4fv(matrixLocation, false, matrix);

  const primitiveType = gl.TRIANGLES;
  const offset = 0;
  const count = 16 * 6;
  gl.drawArrays(primitiveType, offset, count);
  setTimeout(() => {
    render(gl, program, fieldOfViewRadians + 0.1);
  }, 100);
}

